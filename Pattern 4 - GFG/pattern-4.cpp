class Solution {
  public:
    void printTriangle(int n) {
        // code here
        int cnt = 1;
        for(int i=1;i<=n;i++){
            
            int temp = cnt;
            while(temp--){
                cout<<i<<" ";
            }
            cout<<endl;
            cnt++;
        }
    }
};

